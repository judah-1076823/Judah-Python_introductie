# create a list
my_list = [1,2,3,4,5]
print(my_list)

# elk mogelijke datatype is mogelijk
my_list = [1, 2, 3, 'four', 5]
print(my_list)

# elementen hebben een 'index' nummer om hun plek in de lijst aan te geven. Die index begint bij 0
print(my_list[0])

# een element toevoegen achteraan in de lijst
my_list.append(6)
print(my_list)

# of op een specifieke locatie
my_list.insert(3, 'three')
print(my_list)